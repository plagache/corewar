#include "includes/libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*ptr;

	while (lst)
	{
		ptr = lst->next;
		(*f)(lst);
		lst = ptr;
	}
}
