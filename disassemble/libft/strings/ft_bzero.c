#include "../includes/libft.h"

void	*ft_bzero(void *s, size_t n)
{
	char *tab;

	tab = (char *)s;
	if (n == 0)
		return (0);
	while (n-- > 0)
	{
		*tab = 0;
		tab++;
	}
	s = (void *)tab;
	return (s);
}
