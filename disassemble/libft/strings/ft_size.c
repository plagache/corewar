#include "../include/ft_printf.h"

int		ft_size(char *f)
{
	char *tmp;

	tmp = f;
	while (*f != 'd' && *f != 'i' && *f != 'o' && *f != 'u' && *f != 'x'
			&& *f != 'X' && *f != 'c' && *f != 's' && *f != 'p')
		f++;
	return (f - tmp);
}
